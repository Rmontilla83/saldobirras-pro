// Zona especial "Sin Zona"
export const NO_ZONE_ID = 'sin_zona';

export function getZoneById(id: string): StadiumZone | undefined {
  return STADIUM_ZONES.find(z => z.id === id);
}

export function getRowsForZone(zoneId: string): StadiumRow[] {
  return getZoneById(zoneId)?.rows ?? [];
}

export function getSeatsForRow(zoneId: string, rowLabel: string): string[] {
  const zone = getZoneById(zoneId);
  if (!zone) return [];
  return zone.rows.find(r => r.label === rowLabel)?.seats ?? [];
}

export function formatSeatLocation(zone: string | null, row: string | null, seat: string | null): string {
  if (!zone || zone === NO_ZONE_ID) return 'Sin Zona';
  const z = getZoneById(zone);
  const zoneName = z?.name ?? zone;
  const parts = [zoneName];
  if (row) parts.push(`Fila ${row}`);
  if (seat) parts.push(`Asiento ${seat}`);
  return parts.join(' · ');
}
